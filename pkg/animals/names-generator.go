// GetRandomName generates a random name from the list of adjectives and surnames in this package
// formatted as "adjective-surname". For example 'focused-turing'. If retry is non-zero, a random
// integer between 0 and 10 will be added to the end of the name, e.g `focused-turing3`
func GetRandomName(retry int) string {

	right := make([]string, 0, len(animals))
	for _, v := range animals {
		d := strings.Replace(v, " ", "-", -1)
		right = append(right, strings.ToLower(d))
	}

	name := fmt.Sprintf("%s-%s", left[rand.Intn(len(left))], right[rand.Intn(len(right))])

	if retry > 0 {
		name = fmt.Sprintf("%s%d", name, rand.Intn(10))
	}
	return name
}
